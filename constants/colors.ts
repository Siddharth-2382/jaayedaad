function shuffleArray(colorsArray: string[]) {
  for (let i = colorsArray.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [colorsArray[i], colorsArray[j]] = [colorsArray[j], colorsArray[i]];
  }
  return colorsArray;
}

